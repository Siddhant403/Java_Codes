package Conditions_and_Loops;
class Operators{
    public static void main(String args[]){
        int a=10;
        int b=20;
        int sum;
        int diff;
        int mult;
        sum = a + b;
        diff = b - a;
        mult = a * b;
        System.out.println(sum);
        System.out.println(diff);
        System.out.print(mult);
    }
}