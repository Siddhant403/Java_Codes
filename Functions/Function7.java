package Functions;
import java.util.*;

public class Function7 {
    public static int printPrime(int num){
        if(num%num==0){
            System.out.println("Non-Prime Number");
        }
        else{
            System.out.println("Non-Prime Number");
        }
        return 0;
    }
    public static void main(String args[]){
        Scanner scn = new Scanner(System.in);
        int num = scn.nextInt();
        num = printPrime(num);
    }
}
